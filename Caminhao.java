
public class Caminhao extends Motor{
	int peso ;
	int PdaCarga ;
	int Comprimento ;
	
	
	Caminhao () {
	
	}


	public int getPeso() {
		return peso;
	}


	public void setPeso(int peso) {
		this.peso = peso;
	}


	public int getPdaCarga() {
		return PdaCarga;
	}


	public void setPdaCarga(int pdaCarga) {
		PdaCarga = pdaCarga;
	}


	public int getComprimento() {
		return Comprimento;
	}


	public void setComprimento(int comprimento) {
		Comprimento = comprimento;
	}

	
}
